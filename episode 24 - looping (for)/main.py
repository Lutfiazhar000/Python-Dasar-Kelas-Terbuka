# looping

## dengan list
angka = [1,2,3,44,25,16,7]
print(angka)

for i in angka:
    print(f"sekarang i yang ke => {i}")

print("akhir program\n")

## dengan range
angka2 = range(5)

for i in angka2:
    print(f"sekarang i yang ke => {i}")

print("akhir program\n")

## dengan range -> 2
angka3 = range(1,4)

for i in angka3:
    print(f"sekarang i yang ke => {i}")

print("akhir program")

## dengan string
data_str = "saya banyak duit"

for huruf in data_str:
    print(huruf)

print("akhir program\n")


